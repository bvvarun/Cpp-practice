#include <iostream>
using namespace std;

int main() {

	int value = 10;

	switch(value)
	{
	case 4:
		cout << "Value is 4" << endl;
		break;

	case 10:
		cout << "Value is 10" << endl;
		break;

	case 8:
		cout << "Value is 8" << endl;
		break;
	}


	return 0;
}
