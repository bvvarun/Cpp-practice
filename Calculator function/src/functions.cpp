#include <iostream>
using namespace std;
#include "utils.h"

int add(int n1, int n2)
{
	int sum = n1+n2;
	return (sum);
}

int subtract(int n1, int n2)
{
	int diff = n1-n2;
	return (diff);
}

int multiply(int n1, int n2)
{
	int product = n1*n2;
	return (product);
}
